public class wDetails {
    String name, code, credits, teacher, unit, schedule, hours, date;

    public String getName(){
        return name;
    }

    public void setName(String wName){
        this.name = wName;
    }

    public String getCode(){
        return code;
    }

    public void setCode(String wCode){
        this.code = wCode;
    }

    public String getCredits(){
        return credits;
    }

    public void setCredits(String wCredits){
        this.credits = wCredits;
    }

    public String getTeacher(){
        return teacher;
    }

    public void setTeacher(String wTeacher){
        this.teacher = wTeacher;
    }

    public String getUnit(){
        return unit;
    }

    public void setUnit(String wUnit){
        this.unit = wUnit;
    }

    public String getSchedule(){
        return schedule;
    }

    public void setSchedule(String wSchedule){
        this.schedule = wSchedule;
    }

    public String getHours(){
        return hours;
    }

    public void setHours(String wHours){
        this.hours = wHours;
    }

    public String getDate(){
        return date;
    }

    public void setDate(String wDate){
        this.date = wDate;
    }
}
